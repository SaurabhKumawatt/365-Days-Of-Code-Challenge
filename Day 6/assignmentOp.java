public class assignmentOp {
    public static void main(String[] args) {
        // assigning value via assignment operator
        String var1 = "value";
        System.out.println(var1);

        // uninary operator
        int a = 1;
        a++; //increment 1 in a 
        System.out.println(a);

        ++a; // pew increment
        System.out.println(a);

        // decrement in a
        a--;
        System.out.println(a);

        // pre decrement
        --a;
        System.out.println(a);

    }    
}
