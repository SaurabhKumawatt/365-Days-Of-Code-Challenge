public class math {

    public static void main(String[] args) {
        
        //math class 
        int a = 10;
        int b = 15;
        // minimum value print
        System.out.println(Math.min(a, b));

        // maximum value print
        System.out.println(Math.max(a, b));

        // random number generator from 0 to 0.1
        System.out.println(Math.random());
    }
}
