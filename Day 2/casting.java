

public class casting {
    public static void main(String[] args) {
        // casting 

        // implicit casting
        double price = 100.00;
        double fPrice = price + 18.00;
        System.out.println(fPrice);

        // explicit casting
        int p = 100;
        int fP = p + (int)18.0;
        System.out.println(fP);
    }
}
