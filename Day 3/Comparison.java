public class Comparison {
    
    
    public static void main(String[] args) {
        //  comparison operators

        int a = 4;
        int b = 5;
        // is equal to
        System.out.println(a == b);

        //  is not equal to
        System.out.println(a != b);

        // less than
        System.out.println(a < b);

        //  less than equal to
        System.out.println(a <= b);

        // grater than
        System.out.println(a > b);

        // grater than equal to
        System.out.println(a >= b);
    }
}
