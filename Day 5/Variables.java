public class Variables {
    public static void main(String args[]) {
        int a = 10;
        int b = 5;
        System.out.println(a);
        System.out.println(b);

        String name = "Saurabh";
        System.out.println(name);

        b = 105;
        System.out.println(b);

        name = "Saurabh Kumawat";
        System.out.println(name);
    }
    
    
}
